package com.example.morira;


public class Photo {
    private int id;
    private String note;
    private String date;
    private byte[] image;

    public Photo(String note, String date, byte[] image, int id) {
        this.note = note;
        this.date = date;
        this.image = image;
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public byte[] getImage() {
        return image;
    }

    public void setImage(byte[] image) {
        this.image = image;
    }
}
